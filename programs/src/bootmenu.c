// ═══════════════════════════════════════════════════════════════════════════
// VGA TEXT RENDERING
// ═══════════════════════════════════════════════════════════════════════════

static int cursor_x = 0;
static int cursor_y = 0;
static unsigned char text_fg = COL_WHITE;
static unsigned char text_bg = COL_BLACK;

static void vga_put_pixel(int x, int y, unsigned char color) {
    if (x >= 0 && x < FB_WIDTH && y >= 0 && y < FB_HEIGHT)
        FB_BYTE_BASE[y * FB_WIDTH + x] = color;
}

static void vga_draw_char(int x, int y, char c, unsigned char fg, unsigned char bg) {
    int idx = c - 32;
    if (idx < 0 || idx >= 96) idx = 0;
    const unsigned char *glyph = font8x8[idx];
    for (int row = 0; row < 8; row++) {
        unsigned char bits = glyph[row];
        for (int col = 0; col < 8; col++) {
            unsigned char color = (bits & (0x80 >> col)) ? fg : bg;
            vga_put_pixel(x + col, y + row, color);
        }
    }
}

static void vga_print(const char *s) {
    while (*s) {
        char c = *s++;
        if (c == '\n') {
            cursor_x = 0;
            cursor_y += 8;
        } else if (c == '\r') {
            cursor_x = 0;
        } else {
            vga_draw_char(cursor_x, cursor_y, c, text_fg, text_bg);
            cursor_x += 8;
            if (cursor_x >= FB_WIDTH) {
                cursor_x = 0;
                cursor_y += 8;
            }
        }
    }
}

static void vga_set_color(unsigned char fg, unsigned char bg) {
    text_fg = fg;
    text_bg = bg;
}

static void vga_set_cursor(int x, int y) {
    cursor_x = x;
    cursor_y = y;
}

static void clear_screen(void) {
    for (int i = 0; i < FB_WIDTH * FB_HEIGHT; i++)
        FB_BYTE_BASE[i] = COL_BLACK;
    cursor_x = 0;
    cursor_y = 0;
}

// UART functions from soc.h (uart_putc, uart_puts)

// ═══════════════════════════════════════════════════════════════════════════
// HARDWARE TESTS (simplified subset)
// ═══════════════════════════════════════════════════════════════════════════

#define NUM_TESTS 5

static int test_num = 0;
static int tests_passed = 0;

#define VOL(x) ({ volatile unsigned int _v = (x); _v; })

static void print_num(int n) {
    char buf[12];
    int i = 0;
    if (n < 0) {
        vga_print("-");
        n = -n;
    }
    do { buf[i++] = '0' + (n % 10); n /= 10; } while (n > 0);
    while (--i >= 0) {
        char s[2] = {buf[i], 0};
        vga_print(s);
    }
}

static void run_test(const char *name, int result) {
    vga_set_color(COL_WHITE, COL_BLACK);
    char num[4] = {'0' + (test_num+1), ' ', 0};
    vga_print(num);
    vga_print(name);
    
    int len = 0;
    for (const char *p = name; *p; p++) len++;
    for (int i = len; i < 18; i++) vga_print(".");
    
    if (result == 0) {
        vga_set_color(COL_GREEN, COL_BLACK);
        vga_print(" PASS\n");
        tests_passed++;
    } else {
        vga_set_color(COL_RED, COL_BLACK);
        vga_print(" FAIL ");
        print_num(result);
        vga_print("\n");
    }
    test_num++;
}

static int test_timer(void) {
    unsigned int prev = TIMER_COUNT;
    for (int i = 0; i < 100; i++) {
        unsigned int cur = TIMER_COUNT;
        if (cur < prev) return -1;
        prev = cur;
    }
    TIMER_COUNT = 0;
    for (volatile int i = 0; i < 500; i++) ;
    if (TIMER_COUNT == 0) return -2;
    return 0;
}

static int test_rom_coldboot(void) {
    volatile int v0 = sin_table[0];
    volatile int v64 = sin_table[64];
    if (v0 != 0) return -1;
    if (v64 != 0x00010000) return -2;
    return 0;
}

static int test_ram(void) {
    volatile unsigned int *ram = (volatile unsigned int *)RAM_BASE;
    ram[0] = 0xDEADBEEF;
    ram[1] = 0x12345678;
    if (ram[0] != 0xDEADBEEF) return -1;
    if (ram[1] != 0x12345678) return -2;
    return 0;
}

static int test_framebuffer(void) {
    int idx = (FB_HEIGHT - 10) * FB_WIDTH + 100;
    unsigned char orig = FB_BYTE_BASE[idx];
    FB_BYTE_BASE[idx] = 0xAA;
    if (FB_BYTE_BASE[idx] != 0xAA) return -1;
    FB_BYTE_BASE[idx] = orig;
    return 0;
}

static int test_mul_div(void) {
    if (VOL(7) * VOL(6) != 42) return -1;
    if (VOL(42) / VOL(6) != 7) return -2;
    if (VOL(7) % VOL(3) != 1) return -3;
    return 0;
}

static void run_all_tests(void) {
    vga_set_color(COL_YELLOW, COL_BLACK);
    vga_print("=== HARDWARE TESTS ===\n\n");
    
    run_test("Timer", test_timer());
    run_test("ROM cold boot", test_rom_coldboot());
    run_test("RAM", test_ram());
    run_test("Framebuffer", test_framebuffer());
    run_test("MUL/DIV/REM", test_mul_div());
    
    vga_print("\n");
    if (tests_passed == NUM_TESTS) {
        vga_set_color(COL_GREEN, COL_BLACK);
        vga_print("All ");
        print_num(tests_passed);
        vga_print(" tests passed!\n");
    } else {
        vga_set_color(COL_RED, COL_BLACK);
        print_num(NUM_TESTS - tests_passed);
        vga_print(" test(s) failed\n");
    }
}

// ═══════════════════════════════════════════════════════════════════════════
// PROGRAM MENU
// ═══════════════════════════════════════════════════════════════════════════

typedef void (*program_fn)(void);

static const struct {
    const char *name;
    const char *desc;
    program_fn fn;
} programs[] = {
    { "Wolf3D", "Raycaster demo", wolf3d_main },
    // Add more programs here
};

#define NUM_PROGRAMS (sizeof(programs) / sizeof(programs[0]))

static int menu_selected = 0;

static void draw_menu(void) {
    int menu_y = 120;  // Start menu lower on screen
    
    vga_set_cursor(0, menu_y);
    vga_set_color(COL_CYAN, COL_BLACK);
    vga_print("=== SELECT PROGRAM ===\n\n");
    
    for (int i = 0; i < (int)NUM_PROGRAMS; i++) {
        vga_set_cursor(16, menu_y + 16 + i * 16);
        
        if (i == menu_selected) {
            vga_set_color(COL_BLACK, COL_WHITE);  // Inverted for selection
            vga_print("> ");
        } else {
            vga_set_color(COL_WHITE, COL_BLACK);
            vga_print("  ");
        }
        
        vga_print(programs[i].name);
        
        // Pad and show description
        int len = 0;
        for (const char *p = programs[i].name; *p; p++) len++;
        for (int j = len; j < 12; j++) vga_print(" ");
        
        vga_set_color(COL_WHITE, COL_BLACK);
        vga_print(programs[i].desc);
        vga_print("\n");
    }
    
    // Instructions at bottom
    vga_set_cursor(0, FB_HEIGHT - 24);
    vga_set_color(COL_YELLOW, COL_BLACK);
    vga_print("W/S: Navigate   SPACE: Select\n");
    vga_print("(Use keyboard_inject.py for input)");
}

static unsigned char read_key(void) {
    if (KBD_STATUS & 1) {
        return (unsigned char)KBD_DATA;
    }
    return 0;
}

static void menu_loop(void) {
    draw_menu();
    
    while (1) {
        unsigned char key = read_key();
        
        if (key == KEY_W_LOWER || key == KEY_W_UPPER) {
            // Move up
            if (menu_selected > 0) {
                menu_selected--;
                draw_menu();
            }
        } else if (key == KEY_S_LOWER || key == KEY_S_UPPER) {
            // Move down
            if (menu_selected < (int)NUM_PROGRAMS - 1) {
                menu_selected++;
                draw_menu();
            }
        } else if (key == KEY_SPACE || key == KEY_ENTER) {
            // Select program
            uart_puts("Launching: ");
            uart_puts(programs[menu_selected].name);
            uart_puts("\r\n");
            
            // Clear screen and run
            clear_screen();
            programs[menu_selected].fn();
            
            // If program returns (shouldn't for wolf3d), redraw menu
            draw_menu();
        }
        
        // Small delay to avoid hammering keyboard
        for (volatile int i = 0; i < 10000; i++) ;
    }
}

// ═══════════════════════════════════════════════════════════════════════════
// MAIN
// ═══════════════════════════════════════════════════════════════════════════

int main(void) {
    clear_screen();
    
    uart_puts("\r\n=== EECS 3216 BOOT MENU ===\r\n");
    
    // Run hardware tests first
    run_all_tests();
    
    // Show program menu
    menu_loop();
    
    return 0;
}
