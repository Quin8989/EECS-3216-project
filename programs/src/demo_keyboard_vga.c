// ── Screen constants ──────────────────────────────────────────────────────────

#define COLS       40          // FB_WIDTH  / 8 = 320/8
#define ROWS       30          // FB_HEIGHT / 8 = 240/8
#define WORDS_ROW  (FB_WIDTH / 4)  // 80 words per pixel-row

#define FG  0xFFu   // white   RGB332 (7,7,3)
#define BG  0x01u   // dark blue RGB332 (0,0,1)

// ── Low-level pixel drawing ───────────────────────────────────────────────────

// Draw character c at text grid position (col, row).
static void draw_char(int col, int row, char c) {
    if (c < 0x20 || c > 0x7E) c = ' ';
    const unsigned char *glyph = font8x8[(unsigned char)(c - 0x20)];

    int x0 = col * 8;
    int y0 = row * 8;

    for (int r = 0; r < 8; r++) {
        unsigned char bits = glyph[r];
        // Pixels x0..x0+3 packed into one 32-bit word (LSB = leftmost pixel).
        unsigned int w0 = 0, w1 = 0;
        for (int p = 0; p < 4; p++) {
            unsigned char pix = (bits & (0x80u >> p)) ? FG : BG;
            w0 |= (unsigned int)pix << (p * 8);
        }
        for (int p = 0; p < 4; p++) {
            unsigned char pix = (bits & (0x08u >> p)) ? FG : BG;
            w1 |= (unsigned int)pix << (p * 8);
        }
        int idx = (y0 + r) * WORDS_ROW + x0 / 4;
        FB_BASE[idx]     = w0;
        FB_BASE[idx + 1] = w1;
    }
}

// ── Screen helpers ────────────────────────────────────────────────────────────

// Fill the entire framebuffer with the background colour.
static void clear_screen(void) {
    unsigned int bg_word = BG | ((unsigned int)BG << 8)
                              | ((unsigned int)BG << 16)
                              | ((unsigned int)BG << 24);
    int total = FB_HEIGHT * WORDS_ROW;
    for (int i = 0; i < total; i++)
        FB_BASE[i] = bg_word;
}

// Scroll all text rows up by one, clear the bottom row.
static void scroll_up(void) {
    // Copy pixel rows 8..239 → 0..231.
    for (int y = 0; y < FB_HEIGHT - 8; y++) {
        int dst = y * WORDS_ROW;
        int src = dst + 8 * WORDS_ROW;
        for (int x = 0; x < WORDS_ROW; x++)
            FB_BASE[dst + x] = FB_BASE[src + x];
    }
    // Blank the last 8 pixel rows.
    unsigned int bg_word = BG | ((unsigned int)BG << 8)
                              | ((unsigned int)BG << 16)
                              | ((unsigned int)BG << 24);
    for (int y = FB_HEIGHT - 8; y < FB_HEIGHT; y++) {
        int base = y * WORDS_ROW;
        for (int x = 0; x < WORDS_ROW; x++)
            FB_BASE[base + x] = bg_word;
    }
}

// Print a string to the terminal at the current cursor position.
static int cursor_col;
static int cursor_row;

static void put_char(char c) {
    if (c == '\r' || c == '\n') {
        // Carriage return / new line.
        cursor_col = 0;
        cursor_row++;
    } else if (c == '\b' || c == 0x7F) {
        // Backspace: erase previous character.
        if (cursor_col > 0) {
            cursor_col--;
        } else if (cursor_row > 0) {
            cursor_row--;
            cursor_col = COLS - 1;
        }
        draw_char(cursor_col, cursor_row, ' ');
        return;
    } else if (c >= 0x20 && c <= 0x7E) {
        draw_char(cursor_col, cursor_row, c);
        cursor_col++;
        if (cursor_col >= COLS) {
            cursor_col = 0;
            cursor_row++;
        }
    }

    if (cursor_row >= ROWS) {
        scroll_up();
        cursor_row = ROWS - 1;
    }
}

static void put_str(const char *s) {
    while (*s) put_char(*s++);
}

// ── Main ──────────────────────────────────────────────────────────────────────

int main(void) {
    clear_screen();

    cursor_col = 0;
    cursor_row = 0;

    put_str("EECS 3216 RISC-V SoC");
    cursor_col = 0; cursor_row = 1;
    put_str("Keyboard demo via JTAG");
    cursor_col = 0; cursor_row = 2;
    put_str("----------------------");
    cursor_col = 0; cursor_row = 4;
    put_str("Run keyboard_inject.py");
    put_char('\r');
    put_str("then type here:");
    cursor_col = 0; cursor_row = 6;

    while (1) {
        // Wait for a key to be injected via JTAG.
        while (!(KBD_STATUS & 1))
            ;
        char c = (char)(KBD_DATA & 0xFF);
        put_char(c);
    }

    return 0;
}
