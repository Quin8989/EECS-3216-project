// fp_sin / fp_cos: angle is 0..255 = one full revolution.
// cos(a) = sin(a + 90°) = sin(a + 64 steps).
static inline int fp_sin(int a) { return sin_table[(unsigned char)a]; }
static inline int fp_cos(int a) { return sin_table[((unsigned char)a + 64) & 255]; }

// Q16.16 multiply: (a * b) >> 16.
// Compiled to MULH + MUL by GCC -march=rv32im (no library call).
#define FP_MUL(a, b)  ((int)(((long long)(a) * (b)) >> 16))

// Squared magnitude without sqrt — use for ray-length comparisons.
// Both values are in Q16.16, result is Q16.16.
static inline int fp_mag2(int x, int y) { return FP_MUL(x, x) + FP_MUL(y, y); }

